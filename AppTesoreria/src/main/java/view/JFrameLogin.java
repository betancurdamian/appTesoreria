    /**
     * Inicia Session
     */
    private void iniciarSesion() {
        /**
         * setear usuario con datos de la vista
         */
        usuarioAuxiliar.setNombre(getJtf_usuario().getText());
        usuarioAuxiliar.setClave(new String(getJtf_password().getPassword()));

        if (controlador.iniciarSesion(usuarioAuxiliar) == true) {
            //Crea la pantalla principal del sistema
            this.inicio = new JFrameBienvenida();
            inicio.setVisible(true);
            this.dispose();
        } else {
            this.getJlbl_mensaje().setText("ERROR: usuario o Password incorrecto");
            this.limpiar();
        }
    }

