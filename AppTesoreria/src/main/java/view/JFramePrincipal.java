    private void arbolModulosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_arbolModulosMouseClicked

        if (arbolModulos.isEnabled()) {
            TreePath tp = arbolModulos.getPathForLocation(evt.getX(), evt.getY());
            if (tp != null) {
                captura = arbolModulos.getSelectionPath().toString();
                /*Intentanmos validar si la cadena equivale a vacio.
                Si ejecutamos nos daremos cuenta que esto
                genera una excepci√≤n ya que la cadena es nula*/

                if (captura.equals("[Empresa, Administraci√≥n General, Empresa]")) {

                    habilitarArbol(false);
                    
                    //Se crea el Panel Emplesa
                    this.unPanelEmpresa = new PanelEmpresa();
                    this.unPanelEmpresa.setSize(950, 750);
                    this.unPanelEmpresa.setVisible(true);

                    this.jPanelContenido.add(this.unPanelEmpresa);
                    this.unPanelEmpresa.validate();

                }

                if (captura.equals("[Empresa, Administraci√≥n General, Empleados]")) {

                    habilitarArbol(false);

                    //Se crea el Panel Empleado
                    this.unPanelEmpleado = new PanelEmpleado();
                    this.unPanelEmpleado.setSize(950, 750);
                    this.unPanelEmpleado.setVisible(true);

                    this.jPanelContenido.add(this.unPanelEmpleado);

                    this.unPanelEmpleado.validate();
                }

                if (captura.equals("[Empresa, Administraci√≥n General, Usuarios]")) {

                    habilitarArbol(false);

                    //Se crea el Panel Emplesa
                    this.unPanelUsuario = new PanelUsuario();
                    this.unPanelUsuario.setSize(950, 750);
                    this.unPanelUsuario.setVisible(true);

                    this.jPanelContenido.add(this.unPanelUsuario);

                    this.unPanelUsuario.validate();
                }
                
                if (captura.equals("[Empresa, Modulo de Contabilidad, Registrar Movimientos]")) {
                    
                    this.controlador = new ProcesarMovimientosDeCuentaDeEmpresa();
                    habilitarArbol(false);

                    
                    //Se crea el Panel Emplesa
                    this.unPanelSeleccionDeCuenta = new PanelSeleccionDeCuentas(this.controlador);
                    this.unPanelSeleccionDeCuenta.setSize(950, 750);
                    this.unPanelSeleccionDeCuenta.setVisible(true);

                    this.jPanelContenido.add(this.unPanelSeleccionDeCuenta);

                    this.unPanelSeleccionDeCuenta.validate();
                }
                

            }
        }
    }//GEN-LAST:event_arbolModulosMouseClicked


