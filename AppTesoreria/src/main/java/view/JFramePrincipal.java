    public void arranca() {
        this.tipoUsuario = LoginController.getInstanceUsuario().getTipoUsuario();

        if (this.tipoUsuario != null) {
            if (this.tipoUsuario.getDescripcion().equals("Persona")) {
                JOptionPane.showMessageDialog(this, "Los Cliente solo pueden ingresar por la web");
                System.exit(0);
            }

            if (this.tipoUsuario.getDescripcion().equals("Empleado")) {
                //Si el usuario posee un empleado asociado
                if (LoginController.getInstanceUsuario().getUnEmpleado() != null) {

                    //Uso del Singleton creado en el Login
                    this.jlbl_usuario.setText(LoginController.getInstanceUsuario().getNombre());
                    this.jlbl_TipoUsuario.setText(LoginController.getInstanceUsuario().getTipoUsuario().getDescripcion());
                    this.jlbl_puesto.setText(LoginController.getInstanceUsuario().getUnEmpleado().getUnTipoEmpleado().getDescripcion());

                    jPanelContenido.repaint();
                    setLocationRelativeTo(null);// centra la ventana en la pantalla
                    setVisible(true);// visualiza la ventana
                    //default title and icon
                } else {
                    JOptionPane.showMessageDialog(this, "El usuario es de tipo empleado, pero no tiene empleado asociado. Contacte con el Administrador");
                    System.exit(0);
                }
            }
        }
    }

    /**
     * Se llama desde cualquier lugar de la aplicaci√≥n y bloqueara o habilitara
     * el arbol
     *
     * @param estado
     */
    private void habilitarArbol(boolean estado) {
        this.arbolModulos.setEnabled(estado);
        this.jPanelArbol.setEnabled(estado);
        this.jPanelArbol.repaint();
        this.jPanelArbol.validate();

        this.jPanelContenido.removeAll();
        this.jPanelContenido.repaint();
        this.jPanelContenido.validate();

        this.repaint();
        this.validate();
    }

