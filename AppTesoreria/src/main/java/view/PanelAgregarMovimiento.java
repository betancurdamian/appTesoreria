    private void jcb_conceptoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcb_conceptoItemStateChanged
        this.conceptoSeleccionado = (Concepto) jcb_concepto.getSelectedItem();
        if (this.conceptoSeleccionado != null) {
            jtf_monto.setEnabled(true);
            if (conceptoSeleccionado.getUnTipoMovimiento().getDescripcion().equals("Credito")) {
                this.jrb_Credito.setSelected(true);
                this.jrb_Debito.setSelected(false);
            }
            if (conceptoSeleccionado.getUnTipoMovimiento().getDescripcion().equals("Debito")) {
                this.jrb_Credito.setSelected(false);
                this.jrb_Debito.setSelected(true);
            }
        }
        this.jrb_Credito.repaint();
        this.jrb_Debito.repaint();
    }//GEN-LAST:event_jcb_conceptoItemStateChanged

    private void jtf_montoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtf_montoKeyReleased
        if (this.jtf_monto.isEditable()) {
            if (!this.jtf_monto.getText().isEmpty()) {
                habilitarTodosLosBotones(true);
            }else{
                habilitarTodosLosBotones(false);
            }
        }
    }//GEN-LAST:event_jtf_montoKeyReleased


