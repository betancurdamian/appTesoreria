    /**
     * Carga las Areas el el JComboBox Cuentas de Empresa
     *
     */
    public void cargarConceptos() {

        this.conceptosModel.removeAllElements();

        this.conceptosModel = new DefaultComboBoxModel();

        for (Concepto cuentasRecorrido : this.controlador.buscarTodosLosConceptos()) {
            conceptosModel.addElement(cuentasRecorrido);
        }

        this.jcb_concepto.setModel(this.conceptosModel);

        repaint();

    }

